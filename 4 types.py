# Set the variable
s = 'ABC' # string
i = 100 # integer
f = 1.23 # float
b = True # boolean


print(type(s)) # <class 'str'>
print(type(i)) # <class 'int'>
print(type(f)) # <class 'float'>
print(type(b)) # <class 'bool'>

t = []
print(type(t)) # <class 'list'>


print(type(print)) # <class 'builtin_function_or_method'>
print(type(str)) # <class 'type'>