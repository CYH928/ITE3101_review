num = [1, 2, 3, 4, 5, 6, 7, 8, 9]

print(num) # display all of list

print(len(num)) # display list length

print(num[5]) # display single list item

print(num[3:7]) # display from 3 to 6 item

print(num[:7]) # display from first to 6 item

print(num[3:]) # display from 3 to last item

print(num[1:9:3]) # display from 1 to 8 item, but will skip 2 num to next. Really such as range(X:X:3)
