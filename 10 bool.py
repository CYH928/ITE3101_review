print(bool('')) # Empty = False
print(bool(' ')) # Non-empty = True

print(bool(0.0)) # Zero = False, or equal 0 or 0.0 = False
print(bool(1)) # Non-zero = True, or not equal 0 = True

# And(F):
# True and True = True
# True and False = False
# False and True = False
# False and False = False

# Or(T):
# True or True = True
# True or False = True
# False or True = True
# False or False = False

# Not:
# not(True) = False
# not(False) = True

print(3 + 6 / 3) # First, * or /. After, + or -, so ans is 3+(6/3) -> 5