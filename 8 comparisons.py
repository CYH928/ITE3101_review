first = input("first num: ")
second = input("second num: ")

# Six comparisons
if first == second:
    print(first + "=" + second)
if first != second:
    print(first + "≠" + second)
if first < second:
    print(first + "<" + second)
if first > second:
    print(first + ">" + second)
if first <= second:
    print(first + "≤" + second)
if first >= second:
    print(first + "≥" + second)